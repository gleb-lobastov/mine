const mapping = {
  а: 'a',
  б: 'b',
  в: 'v',
  г: 'g',
  д: 'd',
  е: 'e',
  ё: 'e',
  ж: 'j',
  з: 'z',
  и: 'i',
  й: 'y',
  к: 'k',
  л: 'l',
  м: 'm',
  н: 'n',
  о: 'o',
  п: 'p',
  р: 'r',
  с: 's',
  т: 't',
  у: 'u',
  ф: 'f',
  х: 'h',
  ц: 'c',
  ч: 'ch',
  ш: 'sh',
  щ: 'shch',
  ь: '',
  ы: 'y',
  Ъ: '',
  э: 'e',
  ю: 'u',
  я: 'ya',
};

export default function transliteration(text) {
  return Array.from(text)
    .map(char => mapping[char] ?? char)
    .join('');
}
